    internal class PublicKey
    {
        private readonly BigInteger _mod;
        private readonly BigInteger _e;
        private readonly long _fingerPrint;

        public PublicKey(string key)
        {
            var rsa = RSA.Create();
            rsa.ImportFromPem(key);

            var parameters = rsa.ExportParameters(includePrivateParameters: false);

            if (parameters.Modulus == null ||
                parameters.Exponent == null)
                throw new ApplicationException("Invalid RSA key");

            _mod = new BigInteger(parameters.Modulus, isBigEndian: true, isUnsigned: true);
            _e = new BigInteger(parameters.Exponent, isBigEndian: true, isUnsigned: true);

            var hash = SHA1.HashData(GetFingerPrint(parameters.Modulus, parameters.Exponent));
            _fingerPrint = BitConverter.ToInt64(hash.AsSpan()[^8..]);
        }

        private byte[] GetFingerPrint(byte[] modulus, byte[] exponent)
        {
            var keyLength = modulus.Length;
            using var ms = new MemoryStream();
            using var bw = new BinaryWriter(ms);
            bw.Write((byte)254);
            bw.Write((byte)(keyLength & 0xFF));
            bw.Write((byte)((keyLength >> 8) & 0xFF));
            bw.Write((byte)((keyLength >> 16) & 0xFF));
            bw.Write(modulus);

            bw.Write((byte)exponent.Length);
            bw.Write(exponent);

            return ms.ToArray();
        }

        public long FingerPrint => _fingerPrint;

        public byte[] Encrypt(byte[] data)
            => BigInteger.ModPow(
                new BigInteger(data, isBigEndian: true, isUnsigned: true),
                _e, _mod)
            .ToByteArray(isUnsigned: true, isBigEndian: true);
    }
